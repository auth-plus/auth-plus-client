<h3>Pitch Deck</h3>
<p>
	A lot of startups have a dilemma: should I create from zero or should I pay to use? The trade-off
	is simple,
</p>
<ul>
	<li>
		Cons of Creating a new one
		<ul>
			<li>time to waste</li>
			<li>the human effort to code</li>
			<li>the risk of making incorrect/mistakes</li>
			<li>maintenance after done</li>
		</ul>
	</li>
	<li>
		Cons of integration
		<ul>
			<li>a high cost</li>
			<li>the effort to integrate</li>
			<li>a high cost to customize if necessary</li>
		</ul>
	</li>
</ul>
<p>
	Auth+ has small startup as a target, offering all kind of system at low cost, but also offering
	support to customize. Monetizing for each system that the client wants to subscribe
</p>
